# and Operator
# If both are True then only True otherwise only false
print(True and True)
print(True and False)
print(False and True)
print(False and False)
print("\n")


# OR Operator
# Atleast one true, If yes then result will be True otherwise False
print(True or True)
print(True or False)
print(False or True)
print(False or False)


# NOT operator
print("\n")
print( not True )
print(not False)