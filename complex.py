# 1) int to float
# Converting from int to complex
a = complex(10)
print(a)

b = complex(0B1111)
print(b)

# 2) Boolean to complex

d = complex(True)
print(d)

e = complex(False)
print(e)

# 3) Complex to float not possible

c = complex(10.05)
print(c)

# 4) str to float
# Convert from str to float
# string must contain the either int or float datatype in while converting from str to float
e = complex("11")
print(e)

f = complex("11.50")
print(f)

# real and imaginary numbers
g = complex(10,20)
print(g)

h = complex(10.12,20.22)
print(h)