# 1) int to float
# Converting from int to float
a = float(10)
print(a)
b = float(0B1111)
print(b)

# 2) Boolean to float
# Converting from boolean to float
c = float(True)
print(c)
d = float(False)
print(d)

# 3) Complex to float not possible

# 4) str to float
# Convert from str to float
# string must contain the either int or float datatype in while converting from str to float
e = float("10")
print(e)

f = float("10.50")
print(f)